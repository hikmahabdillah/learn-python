y = 6
print(type(y))
# <class 'int'>

y = '6'
print(type(y))
# <class 'str'>

x = 6.0
print(type(x))
# <class 'float'>

# BOOLEAN
#  Boolean data type is used to represent the truth value
z = True
print(type(z))

z = False
print(type(z))

"""
  Output : 
  <class 'bool'>
  <class 'bool'>
"""

# STRING
# String in Python can be defined by using single
# or double quotes. It represents a sequence of characters.
name = 'Particle'
s1 = "Hello "+ name +"!"
print(s1)

# OUTPUT: Hello Particle!